package com.amigoscode.ifstatements;

public class Main {
    public static void main(String[] args) {
        // If Statements
        int age = 21;
        boolean isAdult = age >= 18;
        if (isAdult) {
            System.out.println("You're an adult!");
        } else {
            System.out.println("You're not an adult!");
        }
    }
}
